class SpecialLocation {
  final String title;
  final String description;
  final double latitude;
  final double longitude;
  final String relativeLocation;
  final String type;
  final String image;

  SpecialLocation({
    required this.title,
    required this.description,
    required this.latitude,
    required this.longitude,
    required this.relativeLocation,
    required this.type,
    required this.image,
  });

  // Factory method to create SpecialLocation from JSON
  factory SpecialLocation.fromJson(Map<String, dynamic> json) {
    return SpecialLocation(
      title: json['title'],
      description: json['description'],
      latitude: json['latitude'],
      longitude: json['longitude'],
      relativeLocation: json['relativeLocation'],
      type: json['type'],
      image: json['image'],
    );
  }

  // Convert SpecialLocation to JSON
  Map<String, dynamic> toJson() {
    return {
      'title': title,
      'description': description,
      'latitude': latitude,
      'longitude': longitude,
      'relativeLocation': relativeLocation,
      'type': type,
      'image': image,
    };
  }
}

